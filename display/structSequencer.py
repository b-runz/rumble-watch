# Compress the sequence into a list of {value, count} pairs
def compress_sequence(seq):
    if not seq:
        return []
    
    compressed = []
    current_value = seq[0]
    current_count = 1
    
    for value in seq[1:]:
        if value == current_value:
            current_count += 1
        else:
            compressed.append({"value": current_value, "count": current_count})
            current_value = value
            current_count = 1
    
    # Append the last segment
    compressed.append({"value": current_value, "count": current_count})
    return compressed

# Generate C-style struct output
def print_c_structs(compressed):
    print("static const SequenceSegment sequence[] = {")
    for i, segment in enumerate(compressed):
        print(f"    {{0x{segment['value']:02x}, {segment['count']}}}", end="")
        if i < len(compressed) - 1:
            print(",")
        else:
            print()
    print("};")
    print(f"static const unsigned int sequence_length = {len(compressed)};")

def main():
    if not sequence:
        print("Error: Please paste a non-empty sequence into the 'sequence' list.")
        sys.exit(1)
    
    compressed = compress_sequence(sequence)
    print_c_structs(compressed)
    
    # Verify total length
    total_length = sum(segment["count"] for segment in compressed)
